from solution import move

class TestSolution:

    def test_move(self):
        assert move([["[A]", "[B]", "[C]"], ["[D]", "[E]", "[F]"]], (2, 1, 2), 1) == \
               [["[A]"], ["[D]", "[E]", "[F]", "[C]", "[B]"]]
        assert move([["[A]", "[B]", "[C]"], ["[D]", "[E]", "[F]"]], (2, 1, 2), 2) == \
               [["[A]"], ["[D]", "[E]", "[F]", "[B]", "[C]"]]
        assert move([["[A]", "[B]", "[C]"], ["[D]", "[E]", "[F]"]], (2, 1, 2), 3) == \
               [["[A]"], ["[D]", "[E]", "[F]", "[B]", "[C]"]]
        assert move([["[A]", "[B]", "[C]"], ["[D]", "[E]", "[F]"]], (3, 1, 2), 3) == \
               [[], ["[D]", "[E]", "[F]", "[A]", "[B]", "[C]"]]

